public class WithdrawalDay {
    private Date date;
    private BigDecimal amount;

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public BigDecimal getAmount() {
        return amount;
    }

    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }

    @Override
    public String toString() {
        return "WithdrawalDay{" +
                "date=" + date +
                ", amount=" + amount +
                '}';
    }
}
